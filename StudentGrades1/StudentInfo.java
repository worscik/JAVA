package StudentGrades1;

public class StudentInfo {

    String name;
    String surname;
    String gender;
    double mathGrade;
    double informaticsGrade;
    double physicsGrade;

    public StudentInfo(String name, String surname, String gender, double mathGrade, double informaticsGrade, double physicsGrade) {
        this.name = name;
        this.surname = surname;
        this.gender = gender;
        this.mathGrade = mathGrade;
        this.informaticsGrade = informaticsGrade;
        this.physicsGrade = physicsGrade;
    }
}
